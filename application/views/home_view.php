                        <h3 class="fs-6">Penjelasan singkat tentang alpha, beta dan gamma</h3>
                            <p>Alpha, beta dan gamma adalah parameter yang digunakan untuk melakukan prediksi. 
                                Parameter tersebut digunakan untuk menghitung nilai ramalan. 
                                Nilai ramalan tersebut digunakan untuk memprediksi nilai penjualan pada bulan selanjutnya.</p>
                            <p>Nilai alpha, beta dan gamma dapat diatur sesuai dengan kebutuhan.</p>
                            <p>Alpha membantu alat itu untuk memutuskan seberapa penting informasi baru yang datang. 
                                Jika Alpha besar, artinya alat itu lebih suka mengambil info baru. Jika Alpha kecil, alat itu lebih suka mengingat informasi lama.</p>
                            <p>Beta membantu alat itu untuk memahami apakah ada tren baru atau tidak. Jika Beta besar, artinya alat itu cepat tangkap perubahan tren. 
                                Jika Beta kecil, alat itu lebih santai dan tak mudah terpengaruh oleh tren baru.</p>
                            <p>Gamma membantu alat itu untuk melihat apakah ada pola berulang seperti musim panas, musim hujan, atau musim lainnya. 
                                Jika Gamma besar, alat itu cepat sadar dengan perubahan musiman. Jika Gamma kecil, alat itu lebih fokus pada pola musiman yang sudah lama ada.</p>
                            <p>Nilai alpha, beta dan gamma yang digunakan untuk melakukan prediksi adalah nilai alpha, beta dan gamma yang menghasilkan nilai MAPE terkecil.</p>
